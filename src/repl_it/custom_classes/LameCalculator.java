package repl_it.custom_classes;

public class LameCalculator {

    public int plus(int a, int b) {
        int result = a + b;

        return result;
    }

    public int minus(int a, int b) {
        int result = a - b;
        return result;
    }

    public int multiply(int a, int b) {
        int result = a * b;
        return result;
    }

    public int divide(int a, int b) {
        int result = a / b;
        return result;
    }

}
