package day16_switch_ternary;

public class SwitchPractice {
    public static void main(String[] args) {
        String browser = "Chrome";

        switch (browser) {
            case "Chrome":
                System.out.println("Chrome is loading");
            case "FireFox":
                System.out.println("FireFox is loading");
            case "Edge":
                System.out.println("Edge is loading");

        }
        System.out.println("************************************");








    }
}
