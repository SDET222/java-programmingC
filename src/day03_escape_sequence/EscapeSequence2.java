package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"Iphone case\",\"Iphone\", \"Xbox\",\"webcam\", \"monitor\"");
        System.out.println("\"white cap\", \"black cap\", \"yellow cap\"");
        System.out.println("\"Chimney sweep\"\n\"Dryer vent cleaning\"");
        System.out.println("\"Air Duct Cleaning\"");
        System.out.println("\\\\\\\\\\\\");
        System.out.println("  ^");
        System.out.println("/   \\");
        System.out.println("|   |");
        System.out.println("-----");






    }


}
