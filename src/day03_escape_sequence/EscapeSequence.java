package day03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args){
        System.out.println("\"JAVA\"");
        System.out.println("Lets buy \"coffee\" and \"tea\"");
        System.out.println("'JAVA'\n\\"); // backslash \n goes to new line
        System.out.println("\\\\\\\\\\\\\\");
        System.out.println("java\nIntelliJ");
        System.out.println("\tJava\tSelenium\tAPI"); // backslash \t paragraph space







    }




}
