package test_it_now;

public class LoopPractice {
    public static void main(String[] args) {

        int apples = 1;

        while (apples<=10) {
            System.out.println("Eating apples "+apples );
            apples++;
        }
        System.out.println("No more Apples");
        System.out.println("*************************");

        int count = 1;

        while (count<=15) {
            System.out.println(count + ", ");
            count++;
        }
        System.out.println("Counted");




    }
}
