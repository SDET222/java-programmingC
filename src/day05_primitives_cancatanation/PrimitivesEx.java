package day05_primitives_cancatanation;

public class PrimitivesEx {
    public static void main (String[] args) {
        //  Whole Numbers/ Integer Types
        byte byteValue=127;
        System.out.println(byteValue);
        short shortValue=25000;
        System.out.println(shortValue);
        int intValue=150000;
        System.out.println(intValue);
        long longValue=123000000005l; // always add l at the end
        System.out.println(longValue);
        System.out.println("==============");
        //  Decimal Numbers/ Floating point types
        float floatValue=2555.45f;  // always add f at the end
        System.out.println(floatValue);
        double doubleValue=3559999.11;
        System.out.println(doubleValue);
        System.out.println("==================");
        //Character Type : Char - single character in single quotes ''
        char charValue='R';
        System.out.println(charValue);
        // Boolean - true/false
        boolean booleanValue=true;
        System.out.println("==============================");

        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
        System.out.println("===============");









    }





}
